package Tarefa2;

public class Exercicio2 {

	public static void main(String[] args) {
		int x;
		
		x = 12;
		if(x%2 == 0) {
			System.out.println("Par");
		}
		else {
			System.out.println("Impar");
		}
		
		x = -27;
		if(x%2 == 0) {
			System.out.println("Par");
		}
		else {
			System.out.println("Impar");
		}
		
		x = 0;
		if(x%2 == 0) {
			System.out.println("Par");
		}
		else {
			System.out.println("Impar");
		}
		
	}

}

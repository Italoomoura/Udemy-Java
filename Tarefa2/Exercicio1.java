package Tarefa2;

public class Exercicio1 {

	public static void main(String[] args) {
		int x;
		x = -10;
		if (x < 0) {
			System.out.println("É negativo");
		}
		else {
			System.out.println("Não é negativo");
		}
		
		x = 8;
		if (x < 0) {
			System.out.println("É negativo");
		}
		else {
			System.out.println("Não é negativo");
		}
		
		x = 0;
		if (x < 0) {
			System.out.println("É negativo");
		}
		else {
			System.out.println("Não é negativo");
		}
	}

}
